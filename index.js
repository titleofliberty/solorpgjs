    function shuffle(array) {
        let copy = [...array]; // Create a copy
        for (let i = copy.length - 1; i > 0; i--) {
            const j = Math.floor(Math.random() * (i + 1));
            [copy[i], copy[j]] = [copy[j], copy[i]];
        }
        return copy;
    }

    function getRandomStrings(array, quantity) {
        if (!Array.isArray(array) || array.length === 0) {
            return [];
        }
        if (typeof quantity !== "number" || quantity < 0) {
            quantity = 0;
        }
        const maxQuantity = Math.min(quantity, array.length);
        let ary = shuffle(array)
        return ary.slice(0, maxQuantity);
    }

